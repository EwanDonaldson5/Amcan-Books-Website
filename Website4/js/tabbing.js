
function toggle1() {
 if( document.getElementById("hidethis1").style.display=='none' ){
   document.getElementById("hidethis1").style.display = 'table-row'; // set to table-row instead of an empty string
 }else{
   document.getElementById("hidethis1").style.display = 'none';
 }
}


